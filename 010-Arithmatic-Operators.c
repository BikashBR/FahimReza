#include <stdio.h>

int main(void)
{
/*
    Arithmatic Operator 
    --------------------------
        +, -, *, /, %
*/
    int num1 = 33; 
    int num2 = 5; 

    printf("Value is = %d\n", num1 + num2); //Adds two operands. 
    printf("Value is = %d\n", num1 - num2); //Subtracts second operand from the first. 
    printf("Value is = %d\n", num1 * num2); //Multiplies both operands.
    printf("Value is = %d\n", num1 / num2); //Divides numerator by de-numerator.
    printf("Value is = %d\n", num1 % num2); //Modulus Operator and remainder of after an integer division.

    return 0; 
}

