#include <stdio.h>

/*
    For Loop
    > Execute a block of instruction Repeatedly. 
    > Syntax: for(Initialization; Condition; Modify) 
*/

int main(void)
{
    printf("\n\nControl Statement - For Loop\n\n");

    for(int i=0; i<=5; i++)
    {
        printf("Value is: %d\n", i); 
        
    }


    return 0; 
}

